			<p>Control de Calidad: son todos los mecanismos, acciones, herramientas realizadas para detectar la presencia de errores. </p>
				<p>La función del control de calidad existe primordialmente como una organización de servicio, para conocer las especificaciones establecidas por la ingeniería del producto y proporcionar asistencia al departamento de fabricación, para que la producción alcance estas especificaciones. Como tal, la función consiste en la recolección y análisis de grandes cantidades de datos que después se presentan a diferentes departamentos para iniciar una acción correctiva adecuada.</p>

<p>Todo producto que no cumpla las características mínimas para decir que es correcto, será eliminado, sin poderse corregir los posibles defectos de fabricación que podrían evitar esos costos añadidos y desperdicios de material.</p>

<p>Para controlar la calidad de un producto se realizan inspecciones o pruebas de muestreo para verificar que las características del mismo sean óptimas. El único inconveniente de estas pruebas es el gasto que conlleva el control de cada producto fabricado, ya que se eliminan los defectuosos, sin posibilidad de reutilizarlo. Función Principal -Esta asegura de que sus productos o servicios cumplan con los requisitos mínimos de calidad.</p>
